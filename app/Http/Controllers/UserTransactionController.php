     */
    public function index(Request $request)
    {
        $where = [];
        if (($status = $request->input('status', -1)) >= 0) {
            $where['status'] = $status;
        }

        $items = Transaction::with('items')
            ->where('root', 0)
            ->where($where)
            ->oldest('status')
            ->paginate($this->getPageSize());

        return success($items);
    }

